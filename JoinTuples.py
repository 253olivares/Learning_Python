# Python Join Tuples

# To join two or more tuples you can use the + operator

tuple1 =("a", "b", "c")

tuple2 = (1,2,3)

tuple3 = tuple1+tuple2
print(tuple3)

# Multiply tuples

# IF you want to multiple the content of a tuple givena  number of times you can use the * operator

fruits = ("apple", "banana", "cherry")

mytuple = fruits*2 # this will double the tuple and add the tuple again at the end
print(mytuple)