# Int
# Integer is a while number positive or negative without decimals
x = 1
y = 35656222554887711
z = -3255522

# Float
# Float is a number positive or negative containing one or more decimal
x = 1.1
y = 1.0
z = -35.59

# Floats can also be scientific numbers with e to indicate the power of 10
x = 35e3
y = 12E4
z = -87.7e1000

# Complex
# Complex numbers are written with a j
x = 3+5j
y = 5j
z = -5j

# Type Conversion
# You can convert from one type to another when dealing with numbers
